package com.liepin.rim.org.day01.test07;

public class Xunhuan {

    public static void main(String[] args){
        //使用while循环
        int i = 0;
        while(i < 10){
            System.out.println(i);
            i++;
        }

        //使用for循环
        for (int j = 0; j < 10; j++) {
            System.out.println(i);
        }
    }
}
